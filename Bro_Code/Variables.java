public class Variables {
    public static void main(String[] args){

     //  int x; //declaration

     //  x = 123; //assignment

       int x = 123; //initialization
        double a = 3.14; //no f
        float z = 3.14f; //followed with f
        boolean b = true; //True or False
        char symbol = '@'; //single char
        String name = "Jaime"; //String is a reference data type, begins with capital
        long y = 12310298310938018L; //must be followed with an L

       System.out.println("My number is: " + x);

    }
}
